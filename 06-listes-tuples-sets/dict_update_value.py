"""
MODIFICATION D'UNE VALEUR DANS UN DICTIONNAIRE - V1.0
------------------------------------------------------
Ce script illustre les concepts fondamentaux suivants :
- Accès à une valeur via sa clé.
- Modification d'une valeur existante dans un dictionnaire.
- Affichage du dictionnaire mis à jour.
"""

user = {"nom": "Alice", "age": 25}

# Modification d'une valeur
user["age"] = 30
user["nom"] = "Stéphane"

# Affichage du dictionnaire mis à jour.
print(user)