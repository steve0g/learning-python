# Calcul du périmètre et de l’aire d’un rectangle
# Déclare deux variables : longueur = 8 et largeur = 5.
longueur = 8
largeur = 5
# Calcule et affiche : Le périmètre du rectangle (P = 2 × (longueur + largeur)),  L’aire du rectangle (A = longueur × largeur)
P = 2 * (longueur + largeur)
A = longueur * largeur
print(f"Perimètre du rectange = {P}")
print(f"Aire du rectangle = {A}")

