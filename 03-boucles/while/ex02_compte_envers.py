# Objectif : Afficher les nombres de 5 à 1 en utilisant une boucle while.

compteur = 5
while compteur >= 1:
    print(compteur)
    compteur = compteur - 1 # ou compteur -= 1 on décrémente le compteur de 1 à chaque itération
print("Décollage...")